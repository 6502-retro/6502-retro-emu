.org $800

BIOS_DISPATCH	= $200
BIOS_REBOOT	= BIOS_DISPATCH + 3
BIOS_WBOOT	= BIOS_REBOOT + 3
BIOS_CONOUT	= BIOS_WBOOT + 3
BIOS_CONIN	= BIOS_CONOUT + 3
BIOS_CONST	= BIOS_CONIN + 3
BIOS_CONPUTS	= BIOS_CONST + 3
BIOS_CONBYTE	= BIOS_CONPUTS + 3
BIOS_CONBEEP	= BIOS_CONBYTE + 3
BIOS_SN_START	= BIOS_CONBEEP + 3
BIOS_SN_SILENCE	= BIOS_SN_START + 3
BIOS_SN_STOP	= BIOS_SN_SILENCE + 3
BIOS_SN_SEND	= BIOS_SN_STOP + 3
BIOS_LED_ON	= BIOS_SN_SEND + 3
BIOS_LED_OFF	= BIOS_LED_ON + 3
BIOS_GET_BUTTON = BIOS_LED_OFF + 3
BIOS_ERROR_CODE = BIOS_GET_BUTTON + 3


start:
	jsr do_status
	bcc start

	cmp #'q'
	beq quit
	;inc
	jsr do_write
	bra start

do_read:
	jmp BIOS_CONIN

do_status:
	jmp BIOS_CONST

do_write:
	jmp BIOS_CONOUT
quit:
	jmp $FF02
